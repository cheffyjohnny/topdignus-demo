const SPECIAL_50T = '50T*200*3600 + 50T*400*3600 * 2'

const ROLL_LEN: Record<string, number> = {
  '25T*200*7200': 7200,
  '25T*400*7200': 7200,
  '38T*200*4800': 4800,
  '38T*400*4800': 4800,
  '38T*600*4800': 4800,
  '50T*200*3600': 3600,
  '50T*400*3600': 3600,
}

// key: "internalName|prodKey|sleeve"
const HEAT_LOOKUP = new Map<string, { heatType: string; lengthMm: number }>()
for (const [item, prod, sleeve, heatType, lengthMm] of HEAT_TABLE) {
  HEAT_LOOKUP.set(`${item}|${prod}|${sleeve ?? ''}`, { heatType, lengthMm })
}

export interface HeatCalcRow {
  type: string
  totalMm: number
  rolls: number
}

export interface HeatCalcItemRow {
  internalName: string
  pipeSpec: string
  sleeveSpec: string
  quantity: number
  lengthMmPerItem: number
  totalMm: number
}

export interface HeatCalcGrouped {
  type: string
  rollLengthMm: number
  items: HeatCalcItemRow[]
  totalMm: number
  rolls: number
}

interface CalcItem {
  internalName?: string
  pipeSpec?: string
  sleeveSpec?: string
  quantity?: number
}

export interface SealantCalcItemRow {
  internalName: string
  pipeSpec: string
  sleeveSpec: string
  quantity: number
  volumePerItem: number
  totalVolume: number
}

export interface SealantCalcResult {
  items: SealantCalcItemRow[]
  totalVolume: number
  count: number
}

export function calcSealant(items: CalcItem[], sealantMap: Map<string, number>): SealantCalcResult | null {
  const rows: SealantCalcItemRow[] = []
  let totalVolume = 0

  for (const item of items) {
    const { internalName, pipeSpec, sleeveSpec, quantity } = item
    if (!internalName || !pipeSpec || !quantity) continue

    const key = sleeveSpec ? `${internalName}_${pipeSpec}_${sleeveSpec}` : `${internalName}_${pipeSpec}`
    const vol = sealantMap.get(key)
    if (vol === undefined) continue

    const totalForItem = vol * quantity
    rows.push({ internalName, pipeSpec, sleeveSpec: sleeveSpec ?? '', quantity, volumePerItem: vol, totalVolume: totalForItem })
    totalVolume += totalForItem
  }

  if (rows.length === 0) return null
  return { items: rows, totalVolume, count: Math.ceil(totalVolume) }
}

export function calcHeatInsulator(items: CalcItem[]): HeatCalcRow[] {
  const totals = new Map<string, number>()

  for (const item of items) {
    const { internalName, pipeSpec, sleeveSpec, quantity } = item
    if (!internalName || !pipeSpec || !quantity) continue

    const pipe = normalizePipeForExcel(pipeSpec)

    let key: string
    if (internalName === '원형덕트') {
      key = `원형덕트|${pipe}|`
    } else {
      const sleeve = sleeveSpec ? normalizePipeForExcel(sleeveSpec) : ''
      key = `${internalName}|${internalName}_${pipe}|${sleeve}`
    }

    const entry = HEAT_LOOKUP.get(key)
    if (!entry) continue

    const mm = entry.lengthMm * quantity

    if (entry.heatType === SPECIAL_50T) {
      totals.set('50T_BASE', (totals.get('50T_BASE') ?? 0) + mm)
    } else {
      totals.set(entry.heatType, (totals.get(entry.heatType) ?? 0) + mm)
    }
  }

  const result: HeatCalcRow[] = []

  for (const [type, totalMm] of totals) {
    if (type === '50T_BASE') {
      result.push({
        type: '50T*200*3600',
        totalMm: Math.round(totalMm),
        rolls: Math.ceil(totalMm / ROLL_LEN['50T*200*3600']),
      })
      result.push({
        type: '50T*400*3600',
        totalMm: Math.round(totalMm),
        rolls: Math.ceil((totalMm * 2) / ROLL_LEN['50T*400*3600']),
      })
    } else {
      result.push({
        type,
        totalMm: Math.round(totalMm),
        rolls: Math.ceil(totalMm / (ROLL_LEN[type] ?? 4800)),
      })
    }
  }

  return result.sort((a, b) => a.type.localeCompare(b.type))
}

export function calcHeatInsulatorDetailed(items: CalcItem[]): HeatCalcGrouped[] {
  const groups = new Map<string, { rollLengthMm: number; items: HeatCalcItemRow[] }>()

  for (const item of items) {
    const { internalName, pipeSpec, sleeveSpec, quantity } = item
    if (!internalName || !pipeSpec || !quantity) continue

    const pipe = normalizePipeForExcel(pipeSpec)
    const sleeve = sleeveSpec ? normalizePipeForExcel(sleeveSpec) : ''

    const key = internalName === '원형덕트'
      ? `원형덕트|${pipe}|`
      : `${internalName}|${internalName}_${pipe}|${sleeve}`

    const entry = HEAT_LOOKUP.get(key)
    if (!entry) continue

    const itemRow: HeatCalcItemRow = {
      internalName,
      pipeSpec: pipe,
      sleeveSpec: sleeve,
      quantity,
      lengthMmPerItem: entry.lengthMm,
      totalMm: Math.round(entry.lengthMm * quantity),
    }

    const groupKey = entry.heatType === SPECIAL_50T ? '50T_BASE' : entry.heatType
    const rollLen = entry.heatType === SPECIAL_50T ? 3600 : (ROLL_LEN[entry.heatType] ?? 4800)
    if (!groups.has(groupKey)) groups.set(groupKey, { rollLengthMm: rollLen, items: [] })
    groups.get(groupKey)!.items.push(itemRow)
  }

  const result: HeatCalcGrouped[] = []
  for (const [type, { rollLengthMm, items: gItems }] of groups) {
    const totalMm = gItems.reduce((s, r) => s + r.totalMm, 0)
    if (type === '50T_BASE') {
      result.push({ type: '50T*200*3600', rollLengthMm: 3600, items: gItems, totalMm, rolls: Math.ceil(totalMm / 3600) })
      result.push({ type: '50T*400*3600', rollLengthMm: 3600, items: gItems, totalMm, rolls: Math.ceil((totalMm * 2) / 3600) })
    } else {
      result.push({ type, rollLengthMm, items: gItems, totalMm, rolls: Math.ceil(totalMm / rollLengthMm) })
    }
  }
  return result.sort((a, b) => a.type.localeCompare(b.type))
}
